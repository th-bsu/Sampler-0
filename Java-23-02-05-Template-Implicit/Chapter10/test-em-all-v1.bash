# TH: creates brand new Composite.
function recreateComposite() {
    local productId=$1
    local composite=$2
    assertCurl 200 "curl -X DELETE http://$HOST:$PORT/product-composite/${productId} -s"
    curl -X POST http://$HOST:$PORT/product-composite -H "Content-Type: application/json" --data "$composite"
}

# TH: adds recommendations to existing Composite.
function updateCompositeRecommendation(){
    local composite=$1
    curl -X POST http://$HOST:$PORT/product-composite-recommendation-add -H "Content-Type: application/json" --data "$composite"
}

